const cars = ref<CarData[]>(JSON.parse(JSON.stringify(carData)))

export function useCarData() {

  const totalCars = computed(() => cars.value.length)

  const priceRange = computed(() => {
    const prices = cars.value.map((c) => c.carPrice)
    return {
      min: Math.min(...prices),
      max: Math.max(...prices),
    }
  })

  const modelGroups = computed(() => {
    const groups: Record<string, CarData[]> = {}
    cars.value.forEach((car) => {
      const model = car.carName1
      if (!groups[model]) groups[model] = []
      groups[model].push(car)
    })
    return groups
  })

  const searchCar = (query: string): CarData[] => {
    const q = query.toLowerCase()
    return cars.value.filter(
      (car) =>
        car.carName1.toLowerCase().includes(q) ||
        car.carName2.toLowerCase().includes(q) ||
        car.carPriceText.includes(q)
    )
  }

  return {
    cars,
    totalCars,
    priceRange,
    modelGroups,
    searchCar,
  }
}
